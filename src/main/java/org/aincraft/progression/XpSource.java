package org.aincraft.progression;

/**
 * Enum representing different sources of guild XP.
 */
public enum XpSource {
    /**
     * XP earned from killing hostile mobs.
     */
    MOB_KILL,

    /**
     * XP earned from mining blocks.
     */
    BLOCK_MINING,

    /**
     * XP earned from being online in guild territory.
     */
    PLAYTIME
}
